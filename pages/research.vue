        <h1>DNA Damage Checkpoints</h1>
   DNA Damage Checkpoints are intracellular signaling pathways that delay or arrest cell cycle progression in response to DNA damage and thus aid in preventing mutations and cellular and organismic death.  In human cells, two main DNA damage checkpoints pathways have been defined that are governed by the ATM and ATR protein kinases.  The ATR pathway is activated by UV or by replication block by base damage or by nucleotide depletion.  In line with our interest with cellular response to UV and UV-mimetic agents, we have been studying the ATR-mediated DNA damage checkpoint response using in vitro systems with purified proteins as well as cell-based assays.  Using purified proteins and model DNA substrates that mimic the initiating triggers for DNA damage checkpoint activation, we have reconstituted the bases ATR-dependent signaling reaction.  Importantly, we have succeeded in linking DNA excision repair to the ATR checkpoint pathway (Fig. 2): Using purified core nucleotide excision repair factors (RPA, XPA, XPC, TFIIH, XPG, and XPF-ERCC1), core DNA damage checkpoint proteins (ATR-ATRIP, TopBP1, RPA), and damaged DNA, we coupled excision repair to the ATR-mediated checkpoint.  We found that checkpoint signaling as measured by phosphorylation of target proteins (RPA, p53) required the enlargement of the single-strand gap generated by the excision repair system by the 5’ to 3’ exonuclease activity of EXO1 enzyme in accordance with the in vivo data.  Future work will take advantage of our unique in vitro system to characterize novel regulators of ATR signaling and to screen and validate promising anti-cancer drugs that inhibit the ATR function.
